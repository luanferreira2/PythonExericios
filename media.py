# Tirar a média de uma série de valores valores

a = 20
b = 30
c = 5
d = 12

soma = a + b + c + d

media = soma / 4

print('A media desses numeros é de:', media)

#método diferente de impressão

print(f'\n Número 1: {a}\n Número 2: {b}\n Número 3: {c}\n Número 4: {d}')

print(' \nA media desses numeros é de:', media)

# Calcular média usando input do teclado

a = int(input('\n Digite um número inteiro: '))

