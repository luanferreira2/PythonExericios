# Multiplicar dois números

a = 10
b = 67

mult = a * b

print('\n Número 1: ', a)
print(' Número 2: ', b)

print('\nA multiplicção desses numeros é igual a', mult)

# Multiplicar dois número usando input do teclado

a = int(input('\nDigite um número inteiro: '))

b = int(input('\nDigite um número inteiro: '))

mult = a * b

print(f'\nA multiplicação entre os numeros {a} e {b} é igual a {mult}')
