# Subtrair dois números

a = 20
b = 5

sub = a - b

print('\n Número 1: ', a)
print(' Número 2: ', b)

print('\n A subtração desses numeros é igual a ', sub)

# Subtrair dois número usando input do teclado

a = int(input('\nDigite um número inteiro: '))

b = int(input('\nDigite um número inteiro: '))

sub = a - b

print(f'\nA subtração dos numeros {a} e {b} é igual a {sub}')

